    private void BookTicket_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookTicket_btnActionPerformed
        // TODO add your handling code here:
        
        if (!validateBookingInputs()) {
            return;
        }
        
        // 2. Gather values from the form
        int egyptianAdults = (int) user_EgAdults.getValue();
        int egyptianChildren = (int) user_EgChild.getValue();
        int egyptianStudents = (int) user_EgStudents.getValue();
        int egyptianSeniors = (int) user_EgSenior.getValue();

        int foreignAdults = (int) user_FrAdults.getValue();
        int foreignChildren = (int) user_FrChild.getValue();
        int foreignStudents = (int) user_FrStudent.getValue();

        String datePart = user_SelectDate.getText().trim(); // e.g., "2025-11-29"
        String timePart = "";

        if (jRadioButton1.isSelected()) timePart = jRadioButton1.getText();
        else if (jRadioButton2.isSelected()) timePart = jRadioButton2.getText();
        else if (jRadioButton3.isSelected()) timePart = jRadioButton3.getText();
        else if (jRadioButton4.isSelected()) timePart = jRadioButton4.getText();

        // Combine date and time
        String visitTime = datePart + " " + timePart; // "2025-11-29 08:30 AM"


        // 3. Calculate total price
        Booking_GEM calculator = new Booking_GEM();
        double totalPrice = calculator.calculateBulkPrice(
            egyptianAdults, egyptianChildren, egyptianStudents, egyptianSeniors,
            foreignAdults, foreignChildren, foreignStudents
        );

        // 4. Create the booking object
        handleGEMBookings booking = new handleGEMBookings(
            this.user_id, // assuming you have a user_id field in this JFrame
            egyptianAdults, egyptianChildren, egyptianStudents, egyptianSeniors,
            foreignAdults, foreignChildren, foreignStudents,
            totalPrice, visitTime
        );

        // 5. Save booking to database
        booking.saveBooking();

        // 6. Show confirmation
        // JOptionPane.showMessageDialog(this, "Booking successful!\nTotal Price: EGP " + totalPrice);
        
        JOptionPane.showMessageDialog(this, "Booking successful!");
    }//GEN-LAST:event_BookTicket_btnActionPerformed

