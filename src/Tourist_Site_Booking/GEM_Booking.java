    private boolean validateBookingInputs() {
    String dateText = user_SelectDate.getText();
        if (dateText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a date.");
            return false;
        }

        // Optional: Check date format (e.g., yyyy-MM-dd)
        if (!dateText.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "Date format must be YYYY-MM-DD.");
            return false;
        }

        // Check spinners (must be >=0)
        if ((Integer)user_EgAdults.getValue() < 0 ||
            (Integer)user_EgChild.getValue() < 0 ||
            (Integer)user_EgStudents.getValue() < 0 ||
            (Integer)user_EgSenior.getValue() < 0 ||
            (Integer)user_FrAdults.getValue() < 0 ||
            (Integer)user_FrChild.getValue() < 0 ||
            (Integer)user_FrStudent.getValue() < 0) {

            JOptionPane.showMessageDialog(this, "Number of visitors cannot be negative.");
            return false;
        }

        // Check at least one ticket selected
        int totalVisitors = (Integer)user_EgAdults.getValue() + (Integer)user_EgChild.getValue() +
                            (Integer)user_EgStudents.getValue() + (Integer)user_EgSenior.getValue() +
                            (Integer)user_FrAdults.getValue() + (Integer)user_FrChild.getValue() +
                            (Integer)user_FrStudent.getValue();
        if (totalVisitors == 0) {
            JOptionPane.showMessageDialog(this, "You must select at least one ticket.");
            return false;
        }

        return true; // all validations passed
    }
    
    

